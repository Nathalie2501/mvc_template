	<h1>Ce qui fait l’efficacité d’un soin, c’est la qualité des ingrédients cosmétiques qu’il contient.</h1>



	<h2>LES MEILLEURS INGRÉDIENTS COSMÉTIQUES NATURELS</h2>

	<section> 

	Les bonnes associations à connaître pour débuter : <br><br>
	Huile + eau + émulsifiant. L’émulsifiant est un ingrédient indispensable à la réalisation des crèmes, des pommades ou des laits corporels. Un peu comme l’œuf dans une mayonnaise, il permet de réaliser un mélange homogène.<br><br>

	Huile + beurre végétal. Ces corps gras miscibles sont indispensables à la préparation de baumes, de rouges à lèvres… Appréciés pour leurs vertus nourrissantes, protectrices et réparatrices, ils peuvent être enrichis d’extraits de plantes (solubles dans l’huile) et agrémentés de fragrances ou d’huiles essentielles.<br><br>

	Huiles essentielles + huile végétale. Les huiles essentielles ne doivent jamais être appliquée directement sur la peau. Pour limiter les risques d'irritation lors de massages ou frictions, mélangez-les à des huiles végétales.<br><br>

	Bon à savoir : Les huiles essentielles de cannelle, de clou de girofle, de thym, de sarriette ou d’origan pur peuvent provoquer des brûlures sur les muqueuses et la peau. 


