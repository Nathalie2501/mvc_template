	<h2>La conservation des produits maison</h2>

	Cosmétiques à faire soi-même</br></br>
	 
	Faire soi-même ses cosmétiques n'est pas aussi difficile qu'on pourrait le penser.</br> </br>A vous de jouer !

	Les produits sans eau :</br></br>

	Ils se conservent au moins 6 mois. « Les poudres libres ou compactes, les rouges à lèvres et autres crayons ne craignent pas les développements microbiens et se gardent bien à long terme, à condition d’être rangés à l’abri de la chaleur et de la lumière. » Au fil du temps, les corps gras dans les sticks et crayons peuvent devenir rances. « Si l’odeur est désagréable, âcre et piquante, jetez le produit ! Les poudres peuvent même se garder plus longtemps si elles sont conditionnées dans un poudrier hermétique adapté. »

	Les produits contenant de l’eau :</br></br>

	Ils se conservent moins longtemps, environ 3 mois. « Les fonds de teint crème, les eye-liners sous forme de gels aqueux ou les mascaras sous forme d’émulsion nécessitent l’ajout d’un conservateur antimicrobien, comme le Cosgard, un conservateur synthétique très utilisé en cosmétique bio. »
	L’astuce de pro : « Choisissez une eau faiblement minéralisée, ouverte depuis moins de 48h et conservée au frais après ouverture pour éviter tout problème d’instabilité. » 

	Pour éviter les réactions allergiques ou les irritations</br></br>

	Toujours faire un test dans le pli du coude 48 heures avant la première utilisation d’un produit. « Si vous êtes allergique à certains produits, renseignez-vous bien sur les ingrédients que vous souhaitez utiliser. Et ne tentez pas le diable : sur le contour des yeux, n’utilisez ni huiles essentielles ni parfums. En règle générale, les huiles essentielles sont à utiliser avec modération et précaution, elles sont très puissantes et très actives.» 

	<h2>Les bonnes associations à connaître pour débuter</h2></br>

	Huile + eau + émulsifiant. L’émulsifiant est un ingrédient indispensable à la réalisation des crèmes, des pommades ou des laits corporels. Un peu comme l’œuf dans une mayonnaise, il permet de réaliser un mélange homogène.</br></br>


	Huile + beurre végétal.</br></br>
	 Ces corps gras miscibles sont indispensables à la préparation de baumes, de rouges à lèvres… Appréciés pour leurs vertus nourrissantes, protectrices et réparatrices, ils peuvent être enrichis d’extraits de plantes (solubles dans l’huile) et agrémentés de fragrances ou d’huiles essentielles.</br></br>


	Huiles essentielles + huile végétale.</br></br>
	 Les huiles essentielles ne doivent jamais être appliquée directement sur la peau. Pour limiter les risques d'irritation lors de massages ou frictions, mélangez-les à des huiles végétales.

	Bon à savoir : </br></br>
	Les huiles essentielles de cannelle, de clou de girofle, de thym, de sarriette ou d’origan pur peuvent provoquer des brûlures sur les muqueuses et la peau. 

